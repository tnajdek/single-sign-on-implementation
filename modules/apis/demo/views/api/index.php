<h2>Welcome to the API Demos</h2>

<p>These demos provide usage examples for APIs that use OAuth. To get started, choose a demo from the list. The source code of each demo will be included at the bottom of the screen. When exceptions occur, they will be displayed.</p>

<h3>Demo Variables</h3>
<dl>
	<dt><code>$this->content</code></dt>
	<dd>either the API response or some kind of user input for methods that require user parameters or interaction</dd>

	<dt><code>$this->provider</code></dt>
	<dd>OAuth_Provider instance for the current demo</dd>

	<dt><code>$this->consumer</code></dt>
	<dd>OAuth_Consumer instance for the provider</dd>

	<dt><code>$this->token</code></dt>
	<dd>OAuth_Token_Access instance, when authenticated</dd>
</dl>

